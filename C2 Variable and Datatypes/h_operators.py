# Comparison operator
# Its used for comparison

e = 5<6
print(e)    # true

f = 6>=20
print(f)  # false


g = 5!=5
print(g)  # false


h = 10==10
print(h)  # true


# Logical operators

i = True or False  # true
print(i)

j = True and False   # false
print(j)

k = True and True  # true
print(k)